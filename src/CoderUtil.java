
public class CoderUtil {
	
	//获取整形最大值
	public static int getMax(int num1,int num2){
		int result;
		
		if(num1>num2)
			result=num1;
		else
			result=num2;
		
		return result;
	}

	//获取整形最大值
	public static double getMax(double num1,double num2){
		double result;
			
		if(num1>num2)
			result=num1;
		else
			result=num2;
			
		return result;
	}
}
